package com.itwill01.method;

public class Calculator {
	
	// 덧셈
	public int add (int a, int b) {
		int result = a + b;
		return result;
	}
	
	// 뺄셈
	public int sub (int a, int b) {
		int result = a - b;
		return result;
	}
	
	//곱셈
	public int mul (int a, int b) {
		int result = a * b ;
		return result;
	}

	// 나눗셈
	public int div (int a, int b) {
		int result = a / b;
		return result;
	}
	
}
