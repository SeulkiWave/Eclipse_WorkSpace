
public class OperatorRelational {

	public static void main(String[] args) {
		
		/*
		관계 비교 연산
		
		- 형태: >, <, >=, <=, ==, !=
		- 관계 연산의 결과값은 논리형 데이터이다: true, false
		 */

		
		int a = 10;
		int b = 20;
		boolean result;
		result = a > b;
		System.out.println("10");
		result = a < b;
		result = a >= b;
		result = a <= b;
		result = a == b;
		result = a != b;
		
	}

}
