
public class Encapsulation {
	/*
	 * OOP의 클래스 작성 방법
	 *  - 1. 캡슐화
	 *  - 2. 상속성
	 *  - 3. 다형성 (오버로딩, 오버라이딩, 객체형 변환)
	 *  
	 *  1. 캡슐화
	 *  - 외부 클래스(객체)에서 멤버 필드(변수)에 접근을 막고 
	 *  - 외부 클래스(객체)에서 멤버 메소드를 사용했을 때만 접근할 수 있도록 클래스를 설계하는 방법
	 *  	(외부 클래스에서 멤버 메소드로의 접근을 허용한다~)
	 *  
	 *  - 구현: 멤버 필드(변수)의 접근 제한자를 private
	 *  	  멤버 메소드의 접근 제한자는 public으로 설정한다~
	 *  	< access modify >
	 *  	public ==> 어떤 외부 클래스에서든지 접근 가능
	 *  	private ==> 어떤 외부 클래스에서든지 접근 불가능
	 *  
	 *   
	 */
}
